/**
 * Build mx CSV `code` → MyJob `jobs.category` site id map for WhatsApp Edge recommendation.
 * Mirrors `mapMxCategoryPathToSiteCategory` in src/lib/okMxJobImport.ts.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import Papa from 'papaparse';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

function mapMxCategoryPathToSiteCategory(mxPath) {
  const segs = String(mxPath || '')
    .toLowerCase()
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);
  if (segs.length < 2 || segs[0] !== 'jobs') return 'trades-services';
  const rootSeg = segs[1];
  if (rootSeg === 'call-center-customer-service') return 'call-center-customer-service';
  if (rootSeg === 'healthcare-medical') return 'healthcare-medical';
  if (rootSeg === 'mfg-transport-logistics') return 'mfg-transport-logistics';
  if (rootSeg === 'sales') return 'sales';
  if (rootSeg === 'retail-consumer-products' || rootSeg === 'real-estate-property') return 'sales';
  return 'trades-services';
}

const csvPath = path.join(root, 'public/data/mx-job-categories.csv');
const outPath = path.join(root, 'supabase/functions/whatsapp-webhook/mxCodeToSiteCategoryMap.generated.ts');

const text = fs.readFileSync(csvPath, 'utf8');
const parsed = Papa.parse(text, { header: true, skipEmptyLines: true });
/** @type {Record<string, string>} */
const map = {};
for (const row of parsed.data || []) {
  const code = String(row.code ?? '').trim();
  const p = String(row.path ?? '').trim();
  if (!code || !p) continue;
  map[code] = mapMxCategoryPathToSiteCategory(p);
}

const keys = Object.keys(map).sort();
const lines = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`);

const header = `// AUTO-GENERATED by scripts/generate-mx-code-site-category-map.mjs — do not edit by hand.
// Source: public/data/mx-job-categories.csv (${keys.length} codes)

export const MX_CODE_TO_SITE_CATEGORY: Record<string, string> = {
`;

const footer = `} as const;
`;

fs.writeFileSync(outPath, `${header}${lines.join('\n')}\n${footer}`, 'utf8');
console.log('Wrote', outPath, 'entries', keys.length);
